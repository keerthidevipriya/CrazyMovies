package com.crazy.movies.app.crazymovies.pojo;

public class MyReviewUser {
    String author, content, id, url;

    public MyReviewUser(String author, String content, String id, String url) {
        this.author = author;
        this.content = content;
        this.id = id;
        this.url = url;
    }

    public String getAuthor() {
        return author;
    }

    public String getContent() {
        return content;
    }

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }
}
